#"2D List", представляват лист във лист или с други думи много мерни масиви

food = ["Пица", "Спагети", "Хамбургер"]
Drink = ["Кола", "Фанта", "Вода"]
Desert = ["Сладолед", "Плод"]

dinner = [food, Drink, Desert]

print(dinner) #Отпечатва целия мултилист

print(dinner[0]) #Отпечатва първия под лист

print(dinner[0][2]) #Отпечатва третия елемент, от първия подлист

