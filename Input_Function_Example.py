#Примери за входни данни, подадени от потребителя.

#Методът "input()", чете вхопдни данни подадени от потребителя.
# Тяхния тип, винаги е str(Стринг)
name = input("Какво е твоето име: ")
print("Здравей " + name)

#Четене на число от конзолата, трябва да бъде преобразувано в подходящ тип (int, float)
age = int(input("Каква е вашата възраст: "))
print("Вашата възраст е " + str(age))
age = age + 1
print("Догодина вашата възраст ще е " + str(age))