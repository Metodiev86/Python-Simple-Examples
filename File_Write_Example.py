#Втория артгумент на "open()", се избира режима на работа с файл.
# 1. 'r' означава четене,
# 2. 'w' означаване презаписване на файла (ако файла има някакво съдържание, то ще бъде изтрито и на негово място ще
# се постави, новото съдържание),
# 3. 'a' означава допълване на файла. Новото съдържание ще е долепено към старото.
with open("text1.doc", "w") as My_File:
	My_File.write("----------------ЗАГЛАВИЕ----------------\n")
	My_File.write("Здравей")

