#Примери за преобразуване на типовете, един във друг.

x = 1   #int
y = 2.0 #float
z = "3" #str

print(x)
print(y)
print(z)

print(int(y)) #конвертиране от float в int
print(float(z)) #конвертиране от str(String) във float
print(float(z)*3)
print(str(x) + " " + str(y)) #Конвертиране във str(String)
